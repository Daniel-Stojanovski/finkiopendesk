parsed AS (
    SELECT
        dr.subject_name,
        trim(dep_name) AS dependant_name
    FROM dependency_rules dr
    LEFT JOIN LATERAL (
        SELECT unnest(
            CASE
                WHEN dr.dependants IS NULL THEN ARRAY[]::text[]
                ELSE string_to_array(dr.dependants, ',')
            END
        ) AS dep_name
    ) dep ON TRUE
)

INSERT INTO subject_dependency (program_subject_id, dependant_id)
SELECT DISTINCT
    ps1.program_subject_id,
    ps2.program_subject_id
FROM parsed p
         JOIN subject s1 ON s1.name = p.subject_name
         JOIN program_subject ps1 ON ps1.subject_id = s1.subject_id
         JOIN subject s2 ON s2.name = p.dependant_name
         JOIN program_subject ps2 ON ps2.subject_id = s2.subject_id
WHERE p.dependant_name IS NOT NULL;

